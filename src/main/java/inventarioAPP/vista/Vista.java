    public void imprimirInforme(String prodMayor, String prodMenor, String promPrecios, String totalInventario){
        JOptionPane.showMessageDialog(this,
                "Producto precio mayor: " + prodMayor
                + "\nProducto precio menor: " + prodMenor
                + "\nPromedio precios: " + promPrecios
                + "\nValor del inventario: " + totalInventario,
                "Información", JOptionPane.INFORMATION_MESSAGE);
    }
    
