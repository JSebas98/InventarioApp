    /**
     * Imprime un mensaje de éxito dependiendo de la operación pasada como
     * parámetro.
     * @param operacion "Agregar" o "Borrar"
     */
    public void imprimirMensajeExito(String operacion){
        switch(operacion){
            case "Agregar":
                JOptionPane.showMessageDialog(this,
                    "¡El producto fue agregado exitosamente!", "Información",
                    JOptionPane.INFORMATION_MESSAGE);
                break;
            case "Borrar":
                JOptionPane.showMessageDialog(this,
                    "¡El producto fue borrado exitosamente!", "Información",
                    JOptionPane.INFORMATION_MESSAGE); 
                break;
            case "Actualizar":
                JOptionPane.showMessageDialog(this,
                    "¡El producto fue actualizado exitosamente!", "Información",
                    JOptionPane.INFORMATION_MESSAGE); 
                break;
            default:
                JOptionPane.showMessageDialog(this,
                    "¡La operación fue ejecutada exitosamente!", "Información",
                    JOptionPane.INFORMATION_MESSAGE); 
                break;
        }
    }
    
